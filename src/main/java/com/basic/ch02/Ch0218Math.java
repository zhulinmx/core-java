package com.basic.ch02;

/**
 * Math类：数学相关运算
 * Math中的详细方法，查看Java API进行查询
 */
public class Ch0218Math {
    public static void main(String[] args) {
        System.out.println(Math.E);
        // Math 中的方法，请多查看Java API进行查询
        System.out.println(Math.max(1, 3));
        // 计算机中时间存储：从1970年1月1日0时0分0秒0毫秒 到 目前为止的毫秒数
        // 效率比较高
        System.out.println(Math.PI);
        System.out.println(Math.pow(4.0, 3.0));
        //随机数
        System.out.println(Math.random());

    }
}
