package com.basic.ch01;

/**
 * 标识符
 */
public class Ch0101Identifier {
    public static void main(String[] args) {
        /**
         * 各种变量，类名，方法名
         *
         * 标识符规则
         * - 标识符由字母，下划线，数字，美元符号组成
         * - 标识符由字母，下划线，美元符号开头
         * - Java标识符大小写敏感，长度无限制
         *
         * 起名字规则："见名知意"，且不能与Java关键词冲突
         *
         **/
        int result = 10 + 10;
        System.out.println("hello world, " + result);
    }
}